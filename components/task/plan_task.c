bool mqtt_start_first_time = false;
/***********************************************************************************************************************
* Static Functions
***********************************************************************************************************************/
static void PlantControl_Task(void *pvParameters)
{

	while (1)
	{
		rain_sensor_process();
		led_driver_process();
		if ((mqtt_start_first_time == false) && (deive_data.wifi_status == true))
		{
			mqtt_start_first_time = true;
			mqtt_get_cer();
		}
		// check button here
		vTaskDelay(10 / portTICK_PERIOD_MS);
	}
}

